// ─── Parse AI Response ────────────────────────────────────────────────────────

function parseResponse(content) {
  const result = {
    title: "",
    subtitle: "",
    slides: [],
  };

  const titleMatch = content.match(/^TITLE:\s*(.+)/m);
  result.title = titleMatch?.[1]?.trim() || "Presentation";

  const subtitleMatch = content.match(/^SUBTITLE:\s*(.+)/m);
  result.subtitle = subtitleMatch?.[1]?.trim() || "";

  const rawSlides = content.split(/^SLIDE:/m).slice(1);

  rawSlides.forEach((raw) => {
    const lines = raw.split("\n").map(l => l.trim()).filter(Boolean);
    const titleLine = lines.find(l => l.startsWith("Title:"));
    const typeLine  = lines.find(l => l.startsWith("Type:"));

    const title = titleLine?.replace("Title:", "").trim() || "Slide";
    const type  = typeLine?.replace("Type:", "").trim().toLowerCase() || "bullets";

    const items = lines
      .filter(l => l.startsWith("- "))
      .map(l => l.replace(/^-\s*/, "").trim());

    // For STATS slides: "Label | Value"
    const stats = items.map(i => {
      const parts = i.split("|").map(p => p.trim());
      return parts.length === 2
        ? { label: parts[0], value: parts[1] }
        : { label: i, value: "" };
    });

    result.slides.push({ title, type, items, stats });
  });

  return result;
}

// ─── Agent ────────────────────────────────────────────────────────────────────

export const pptAgent = async (state) => {
  try {

    await checkAgentLimit(
        state.userId,
        "ppt"
      );

      await deductCredits(

        state.userId,

        "ppt"

    ); 
    const llm = getModel("ppt");

    const aiResponse = await llm.invoke(`
Create a professional PowerPoint presentation on the topic below.

Topic: ${state.prompt}

Output Rules:
- No markdown, no explanation, only the format below.
- Generate exactly 8 slides after the title.
- Slide types: bullets | stats | conclusion
  - bullets: 4-6 bullet points (most slides)
  - stats: 3-4 key metrics as "Label | Value" (use once for impact)
  - conclusion: 3-4 short takeaway points (last slide)

Format:

TITLE: Presentation Title
SUBTITLE: One-line tagline or description

SLIDE:
Type: bullets
Title: Introduction
- Point one
- Point two
- Point three
- Point four

SLIDE:
Type: stats
Title: Key Numbers
- Users Reached | 10M+
- Revenue Growth | 42%
- Countries | 30+

SLIDE:
Type: conclusion
Title: Conclusion
- Key takeaway one
- Key takeaway two
- Key takeaway three
`);

    const content = aiResponse.content.trim();
    const parsed  = parseResponse(content);

    const ppt = new pptxgen();
    ppt.layout  = "LAYOUT_WIDE";
    ppt.author  = "CortexAI";
    ppt.subject = state.prompt;
    ppt.title   = parsed.title;

    // Cover
    addCoverSlide(ppt, parsed.title, parsed.subtitle);

    // Content slides
    parsed.slides.forEach((slide, i) => {
      const num = i + 1;
      if (slide.type === "stats") {
        addStatSlide(ppt, num, slide.title, slide.stats);
      } else if (slide.type === "conclusion") {
        addConclusionSlide(ppt, slide.title, slide.items);
      } else {
        addBulletSlide(ppt, num, slide.title, slide.items);
      }
    });

    // Save
   const fileName =
  `ppt-${Date.now()}.pptx`;

const buffer =
  await ppt.write({
    outputType: "nodebuffer"
  });

await uploadToS3(
  buffer,
  fileName,
  "application/vnd.openxmlformats-officedocument.presentationml.presentation"
);

const downloadUrl =
  await getDownloadUrl(
    fileName,
    24*60*60
  );

    return {
  ...state,

  response: `
# ✅ Presentation Generated Successfully

📊 **${parsed.title}**

📥 [Download PPT](${downloadUrl})

⏳ Link expires in 10 minutes.
`
};
  } catch (error) {
    console.log("PPT Agent Error:", error);
    return { ...state, response: "Failed to generate presentation." };
  }
};